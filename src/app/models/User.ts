export class User {

    
    
    name:any;
    username:any;
    email:any;
    phone:any
    password:any;
    type:any;

    

    constructor(name:any,username:any,email:any,phone:any,password:any,type:any){
        
        this.username=username
        this.name=name
        this.email=email
        this.phone=phone
        this.password=password
        this.type =type
        
    }

}
